# Демонстрация различий между sort() и sorted()

# sort() - метод списка, изменяет исходный список
arr1 = [5, 2, 8, 1, 9]
print("\nОригинальный список arr1:", arr1)
arr1.sort()  # Метод sort() изменяет список на месте
print("После arr1.sort():", arr1)
print("Оригинальный список изменился!")

print()

# sorted() - функция, создает новый отсортированный список
arr2 = [5, 2, 8, 1, 9]
print("Оригинальный список arr2:", arr2)
arr3 = sorted(arr2)  # Функция sorted() возвращает новый список
print("После arr3 = sorted(arr2):")
print("  arr2 (оригинал):", arr2)
print("  arr3 (новый список):", arr3)
print("Оригинальный список не изменился, создан новый!")